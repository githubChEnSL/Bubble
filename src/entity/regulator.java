package entity;

public class regulator {
	private String regulatorId;
	private String regulatorName;
	private String password;
	private String regulatorRoleId;
	private String storeId;
	public String getRegulatorId() {
		return regulatorId;
	}
	public void setRegulatorId(String regulatorId) {
		this.regulatorId = regulatorId;
	}
	public String getRegulatorName() {
		return regulatorName;
	}
	public void setRegulatorName(String regulatorName) {
		this.regulatorName = regulatorName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRegulatorRoleId() {
		return regulatorRoleId;
	}
	public void setRegulatorRoleId(String regulatorRoleId) {
		this.regulatorRoleId = regulatorRoleId;
	}
	public String getStoreId() {
		return storeId;
	}
	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}
}
